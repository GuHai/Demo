    @Override
    public void onMessage(Message message, byte[] pattern) {
//        byte[] body = message.getBody();
//        String msgBody = (String) this.redisTemplate.getValueSerializer().deserialize(body);
        byte[] channel = message.getChannel();
        String msgChannel = (String)this.redisTemplate.getKeySerializer().deserialize(channel);
//        String msgPattern = new String(pattern);
        if(msgChannel.equals("RefreshAccessToken")){
            DictCacheService.refreshAccessToken();
        }else if(msgChannel.equals("MessageNotice")){
            byte[] body = message.getBody();
            String msgBody = this.redisTemplate.getStringSerializer().deserialize(body);
            com.example.demo.type.Message message1 = JSON.parseObject(msgBody,new TypeReference<com.example.demo.type.Message>(){});
            RedisUtil.sendMessageTemplate(message1);
        }
    }
