    public  <ENTITY extends BaseEntity> boolean addMatchByLike(final List<ENTITY> list,String key,String[] fields) {
        Class clazz = list.get(0).getClass();
        Map map = new HashMap();
        for (ENTITY entity : list) {
            String keystr = key+":";
            for(String fd : fields){
                try {
                    Field field  = clazz.getDeclaredField(fd);
                    field.setAccessible(true);
                    Object obj  = field.get(entity);
                    if(obj!=null){
                        keystr += field.get(entity);
                    }
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
            map.put(keystr,entity.getId());
        }
        this.redisTemplate.opsForValue().multiSet(map);
        return true;
    }


     public  PageParam idl2Page(String[] list ,PageParam pageParam ){
        List<String> ids =   new ArrayList<String>();
        int pageSize = pageParam.getPageSize();//默认加上1，便于计算是否有下一页
        int pageNow = pageParam.getCurrentPage();
        if(list.length>0) {
            int start = pageSize * (pageNow - 1);
            int end = Math.min(pageSize * pageNow+1, list.length);
            for(int i=start;i<end;i++){
                ids.add(list[i]);
            }
            pageParam.setList(ids);
        }
        pageParam.setNextPage(pageParam.getCurrentPage()+1);
        pageParam.setTotalPage(pageParam.getCurrentPage()+ (pageParam.getList().size()>pageSize?1:0));
        if(ids.size()>10){
            pageParam.getList().remove(pageParam.getList().size()-1);
        }
        return pageParam;
    }


